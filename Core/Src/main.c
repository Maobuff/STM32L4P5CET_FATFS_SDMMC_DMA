/*
 * Simple test function to create file "STM32.TXT"
 * write text "hello from stm32!!!"
 * and close file.
 */
void test(void)
{
	if (f_mount(&USERFatFS, (TCHAR const*) USERPath, 0) != FR_OK) {
		printf("app_sd_test: f_mount error\n");
		return;
	}

	if (f_open(&USERFile, "STM32.TXT", FA_CREATE_ALWAYS | FA_WRITE) != FR_OK) {
		printf("app_sd_test: f_open error\n");
		return;
	}

	uint8_t text[] = "hello from stm32!!!";
	uint32_t bw;
	FRESULT res = f_write(&USERFile, text, sizeof(text), &bw);
	if (res != FR_OK || bw == 0) {
		printf("app_sd_test: f_write error\n");
		return;
	}
	f_close(&USERFile);

	printf("app_sd_test: File STM32.TXT created\n");
}
